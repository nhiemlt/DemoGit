    void dangNhap() {
        String username = txtTenDangNhap.getText();
        String password = new String(txtMatKhau.getPassword());
        NhanVien nhanVien = dao.selectById(username);
        if (nhanVien == null) {
            MsgBox.alert(this, "Sai username!");
        } else if (!password.equals(nhanVien.getMatKhau())) {
            MsgBox.alert(this, "Sai password!");
        } else {
            Auth.user = nhanVien;
            this.dispose();
            new ChaoDialog(this, rootPaneCheckingEnabled).setVisible(true);
        }
    }

