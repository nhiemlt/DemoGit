    private void lblEyeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblEyeMouseClicked
        ImageIcon icon1 = new ImageIcon(getClass().getResource("/icons/eye.png"));
        ImageIcon icon2 = new ImageIcon(getClass().getResource("/icons/hide.png"));
        if (showPass == false) {
            txtMatKhau.setEchoChar((char) 0);
            showPass = true;
            lblEye.setIcon(icon2);
        } else {
            txtMatKhau.setEchoChar('*');
            showPass = false;
            lblEye.setIcon(icon1);
        }
    }//GEN-LAST:event_lblEyeMouseClicked

